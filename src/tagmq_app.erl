-module(tagmq_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_Type, _Args) ->
  Dispatch = cowboy_router:compile([
    {'_', [{"/", root_http_handler, []}]}
  ]),
  {ok, _} = cowboy:start_clear(http, 100, [{port, 8080}], #{
		env => #{dispatch => Dispatch}
	}),
  tagmq_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
  ok.

